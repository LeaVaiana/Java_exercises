package operadores_aritmeticos;

public class operadores_aritmeticos {

	public static void main(String[] args) {
		int a = 5;
        int b = 2;
        
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
        //System.out.println(a/0);  // ERROR!!

	}

}
