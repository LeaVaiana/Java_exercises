package for_loop;

public class For_loop {

	public static void main(String[] args) {
		
		//for loop
		for (int i = 0; i < 10; i++) {
            System.out.println("Hola");
        }
    
		//while loop
        int j=0;
        while( j<10 )
        {
            System.out.println("Ciao");

            j++;
        }
        

	}

}
